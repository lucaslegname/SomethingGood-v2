<h3>A propos de Something Good :)</h3>

<p>
Si vous cherchez le sujet principal de Something Good, j'ai bien peur que vous ne trouverez de réponse ni ici, ni ailleurs. En fait, ce site parle de beaucoup de choses différentes: au début, le focus était mis sur la culture urbaine, mais les choses ont un peu changé depuis.
</p>
<p>
Actuellement, c'est le côté blog perso qui a pris le dessus, ceci étant évidemment dû à mon départ de la France en janvier dernier pour venir passer 11 mois ici aux Etats-Unis. Ne vous étonnez donc pas si le site ressemble à un journal de bord encore pour quelques mois, c'est normal.
</p>
<p>
Malgré tout, j'essaie tout de même de vous abreuver, vous mes lecteurs, de quelques sons sympas majoritairement via le compte twitter du site qui est dupliqué dans la side-bar. N'hésitez-pas à y jeter un coup d'oeil, vous pourriez y découvrir de nouveaux artistes intéressants, souvent d'ailleurs des artistes commençant à percer aux US et rencontrés durant les nombreux concerts qui rythment ma vie ici.
</p>

